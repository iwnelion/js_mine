function add7(num){
    return num+7
}
console.log(add7(10))

function multiply(n1,n2){
    return n1*n2
}
console.log(multiply(10,20))

function capitalize(txt){
    return txt.toUpperCase()
}
console.log(capitalize("meow"))

function lastletter(otherTxt){
    return otherTxt.slice(-1)
}
console.log(lastletter("cat"))
